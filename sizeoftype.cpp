#include <iostream>
using namespace std;

int main()
{
	int integer;
	char ch;
	float f;
	double d;
	
	cout << "Size of int is: " <<
	sizeof(integer) <<"\n";

	cout << "Size of char is: " <<
	sizeof(ch) <<"\n";
	
	cout << "Size of float is: " <<
	sizeof(f) <<"\n";

	cout << "Size of double is: " <<
	sizeof(d) <<"\n";

	return 0;
}

